async function handleViewCounter() {
    //  Check 
    if (typeof config === 'undefined' || !config.viewCounter || !config.viewCounter.enabled) return;

    const blobId = config.viewCounter.jsonBlob.blobId;
    const apiUrl = "https://api.jsonblob.com/api/jsonBlob/";
    const storageKey = `has_viewed_${blobId}`;

    try {
        //  Fetch 
        const response = await fetch(`${apiUrl}${blobId}`);
        if (!response.ok) return;
        
        let data = await response.json();
        let currentTotal = data.total || 0;

     
        const hasViewed = localStorage.getItem(storageKey);

        if (!hasViewed) {
            // New visitor: increment the count
            currentTotal += 1;
            
            // Update the JSON Blob on the server
            await fetch(`${apiUrl}${blobId}`, {
                method: 'PUT',
                headers: { 'Content-Type': 'application/json' },
                body: JSON.stringify({
                    total: currentTotal,
                    lastUpdate: new Date().toISOString()
                })
            });

            // Mark this person as "counted" so they can't add more
            localStorage.setItem(storageKey, 'true');
        }

        // Update 
        const counterElement = document.getElementById('viewCount'); 
        if (counterElement) {
            counterElement.textContent = currentTotal;
        }

    } catch (error) {
        console.error("View counter error:", error);
    }
}

document.addEventListener('DOMContentLoaded', handleViewCounter);