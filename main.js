function setupCustomCursor() {
    if (!config.mouseEffects.enabled || !config.mouseEffects.cursorUrl) {
        return;
    }
    
    customCursor = document.createElement('div');
    customCursor.id = 'custom-cursor';
    customCursor.style.position = 'fixed';
    customCursor.style.width = `${config.mouseEffects.cursorSize}px`;
    customCursor.style.height = `${config.mouseEffects.cursorSize}px`;
    customCursor.style.backgroundImage = `url('${config.mouseEffects.cursorUrl}')`;
    customCursor.style.backgroundSize = 'contain';
    customCursor.style.backgroundRepeat = 'no-repeat';
    customCursor.style.backgroundPosition = 'center';
    customCursor.style.pointerEvents = 'none';
    customCursor.style.zIndex = '9999999';
    customCursor.style.transition = 'transform 0.1s ease, opacity 0.3s ease';
    customCursor.style.filter = 'drop-shadow(0 0 4px rgba(255, 255, 255, 0.8))';
    customCursor.style.willChange = 'transform, left, top';
    customCursor.style.opacity = '0';
    
    document.body.appendChild(customCursor);
    
    document.body.style.cursor = 'none';
    
    document.addEventListener('mousemove', (e) => {
        if (customCursor && !isCursorHidden) {
            customCursor.style.left = `${e.clientX - config.mouseEffects.cursorSize / 2}px`;
            customCursor.style.top = `${e.clientY - config.mouseEffects.cursorSize / 2}px`;
            
            if (customCursor.style.opacity !== '1') {
                customCursor.style.opacity = '1';
            }
        }
    });
    
    document.addEventListener('mouseleave', () => {
        if (customCursor) {
            isCursorHidden = true;
            customCursor.style.opacity = '0';
        }
    });
    
    document.addEventListener('mouseenter', () => {
        if (customCursor) {
            isCursorHidden = false;
            customCursor.style.opacity = '1';
        }
    });
    
    const hoverElements = document.querySelectorAll('a, button, .ctrl, .link, .spotify-widget, .discord-presence');
    hoverElements.forEach(element => {
        element.addEventListener('mouseenter', () => {
            if (customCursor) {
                customCursor.style.transform = 'scale(1.3) rotate(10deg)';
                customCursor.style.filter = 'drop-shadow(0 0 6px rgba(255, 255, 255, 1)) brightness(1.2)';
            }
        });
        
        element.addEventListener('mouseleave', () => {
            if (customCursor) {
                customCursor.style.transform = 'scale(1) rotate(0deg)';
                customCursor.style.filter = 'drop-shadow(0 0 4px rgba(255, 255, 255, 0.8)) brightness(1)';
            }
        });
    });
    
    document.addEventListener('mousedown', () => {
        if (customCursor) {
            customCursor.style.transform = 'scale(0.9) rotate(-10deg)';
        }
    });
    
    document.addEventListener('mouseup', () => {
        if (customCursor) {
            customCursor.style.transform = 'scale(1) rotate(0deg)';
        }
    });
}

