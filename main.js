function initializeBackgroundMusic() {
    if (!config.general.backgroundMusic || !config.general.backgroundMusic.enabled) {
        return;
    }
    
    // Create background audio element
    backgroundAudio = new Audio(config.general.backgroundMusic.url);
    backgroundAudio.volume = config.general.backgroundMusic.volume || 0.3;
    backgroundAudio.loop = config.general.backgroundMusic.loop || true;
    
    // Set initial state
    backgroundMusicEnabled = config.general.backgroundMusic.autoplay || false;
    
    // Create background music button
    createBackgroundMusicButton();
    
    // Handle audio errors
    backgroundAudio.addEventListener('error', function() {
        console.error('Error loading background music:', config.general.backgroundMusic.url);
        if (backgroundMusicButton) {
            backgroundMusicButton.style.display = 'none';
        }
    });
    
    // Handle audio loading
    backgroundAudio.addEventListener('canplaythrough', function() {
        if (backgroundMusicEnabled && !isOverlayVisible()) {
            playBackgroundMusic();
        }
    });
    
    // Try to load the audio
    backgroundAudio.load();
}

