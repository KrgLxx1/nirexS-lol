function toggleBackgroundMusic() {
    if (!backgroundAudio) return;
    
    backgroundMusicEnabled = !backgroundMusicEnabled;
    
    if (backgroundMusicEnabled) {
        playBackgroundMusic();
    } else {
        pauseBackgroundMusic();
    }
    
    // Update button appearance
    updateBackgroundMusicButton();
    
    // Update tooltip
    backgroundMusicButton.title = 'Background Music: ' + (backgroundMusicEnabled ? 'ON' : 'OFF');
}

function playBackgroundMusic() {
    if (!backgroundAudio) return;
    
    // Try to play with promise for modern browsers
    const playPromise = backgroundAudio.play();
    
    if (playPromise !== undefined) {
        playPromise.then(() => {
            backgroundMusicEnabled = true;
            updateBackgroundMusicButton();
        }).catch(error => {
            console.log('Background music play failed:', error);
            backgroundMusicEnabled = false;
            updateBackgroundMusicButton();
            
            // Try again with user interaction
            document.addEventListener('click', function tryPlayAgain() {
                backgroundAudio.play().then(() => {
                    backgroundMusicEnabled = true;
                    updateBackgroundMusicButton();
                }).catch(e => {
                    console.log('Second attempt failed:', e);
                });
                document.removeEventListener('click', tryPlayAgain);
            });
        });
    }
}

function pauseBackgroundMusic() {
    if (backgroundAudio && !backgroundAudio.paused) {
        backgroundAudio.pause();
        backgroundMusicEnabled = false;
        updateBackgroundMusicButton();
    }
}

