function startTitleAnimation() {
    // Check if title effect is enabled
    if (!config.general.titleEffect) {
        document.title = config.general.title || "";
        return;
    }
    
    const titleText = config.general.title || "";
    let currentIndex = 0;
    let isTyping = true;
    let effectType = 0; // 0 = normal, 1 = fast, 2 = reverse
    
    // Clear any existing timeout
    if (titleTimeout) clearTimeout(titleTimeout);
    
    function typeEffect() {
        if (effectType === 0) {
            // Normal typing effect
            if (currentIndex <= titleText.length) {
                document.title = titleText.substring(0, currentIndex) + (currentIndex < titleText.length ? "▌" : "");
                currentIndex++;
                titleTimeout = setTimeout(typeEffect, 150);
            } else {
                // Pause at end
                titleTimeout = setTimeout(() => {
                    effectType = 1;
                    currentIndex = 0;
                    typeEffect();
                }, 2000);
            }
        } else if (effectType === 1) {
            // Fast blink effect
            let blinkCount = 0;
            function blink() {
                if (blinkCount < 6) {
                    document.title = titleText + (blinkCount % 2 === 0 ? " ▌" : " ");
                    blinkCount++;
                    titleTimeout = setTimeout(blink, 300);
                } else {
                    effectType = 2;
                    currentIndex = titleText.length;
                    typeEffect();
                }
            }
            blink();
        } else {
            // Reverse delete effect
            if (currentIndex >= 0) {
                document.title = titleText.substring(0, currentIndex) + (currentIndex > 0 ? "▌" : " ");
                currentIndex--;
                titleTimeout = setTimeout(typeEffect, 100);
            } else {
                // Restart cycle
                setTimeout(() => {
                    effectType = 0;
                    currentIndex = 0;
                    typeEffect();
                }, 1000);
            }
        }
    }
    
    typeEffect();
}

