function initializeSpotifyWidget() {
    const spotifyWidget = document.getElementById('spotify-widget');
    
    if (!spotifyWidget) return;
    
    // Add click event to open playlist
    spotifyWidget.addEventListener('click', function() {
        window.open(`https://open.spotify.com/playlist/6VHAp67mDd3YerST4qsOpJ`, '_blank');
    });
    
    // Add title for tooltip
    spotifyWidget.title = "";
    
    // Add hover effect classes
    spotifyWidget.addEventListener('mouseenter', function() {
        this.style.transform = 'translateY(-30px) scale(1.02)';
    });
    
    spotifyWidget.addEventListener('mouseleave', function() {
        this.style.transform = 'translateY(-30px) scale(1)';
    });
    
    // Update widget with config data if available
    if (typeof config !== 'undefined' && config.spotifyWidget) {
        const playlist = config.spotifyWidget.playlist;
        
        // Update playlist name
        const playlistNameEl = document.querySelector('.spotify-playlist-name');
        if (playlistNameEl) {
            playlistNameEl.textContent = playlist.name;
        }
        
        // Update playlist artist/creator
        const playlistArtistEl = document.querySelector('.spotify-playlist-artist');
        if (playlistArtistEl) {
            playlistArtistEl.textContent = playlist.creator;
        }
        
        // Update meta data
        const savesEl = document.querySelector('.spotify-saves');
        const songsEl = document.querySelector('.spotify-songs');
        
        if (savesEl) savesEl.textContent = `${playlist.saves} saves`;
        if (songsEl) songsEl.textContent = `${playlist.songs} songs`;

        
        // Update tooltip
        spotifyWidget.title = `${playlist.name}`;
    }
}

document.addEventListener('DOMContentLoaded', initializeSpotifyWidget);