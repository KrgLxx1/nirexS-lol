function initialize3DEffect() {
    let isHovering = false;
    let targetX = 0;
    let targetY = 0;
    let currentX = 0;
    let currentY = 0;
    const rotationAmount = config.tiltEffect.rotation || 15;
    const verticalRotationAmount = config.tiltEffect.verticalRotation || 12;
    const smoothness = config.tiltEffect.smoothness || 0.15;
    
    function animateTilt() {
        currentX += (targetX - currentX) * smoothness;
        currentY += (targetY - currentY) * smoothness;
        
        c3dElement.style.transform = `
            rotateX(${currentY}deg) 
            rotateY(${currentX}deg)
            translateZ(${Math.abs(currentX + currentY) * 0.3}px)
        `;
        
        requestAnimationFrame(animateTilt);
    }
    
    animateTilt();
    
    function handleMouseMove(event) {
        isHovering = true;
        const elementRect = c3dElement.getBoundingClientRect();
        const mouseX = event.clientX - elementRect.left;
        const mouseY = event.clientY - elementRect.top;
        const normalizedX = (mouseX / elementRect.width) - 0.5;
        const normalizedY = (mouseY / elementRect.height) - 0.5;
        targetX = normalizedX * rotationAmount;
        targetY = normalizedY * -verticalRotationAmount;
    }
    
    function resetTilt() {
        isHovering = false;
        targetX = 0;
        targetY = 0;
    }
    
    c3dElement.addEventListener('mousemove', handleMouseMove);
    c3dElement.addEventListener('mouseleave', resetTilt);
    mainCardElement.addEventListener('mousemove', handleMouseMove);
    playerElement.addEventListener('mousemove', handleMouseMove);
    mainCardElement.addEventListener('mouseleave', resetTilt);
    playerElement.addEventListener('mouseleave', resetTilt);
    
    let isTouching = false;
    c3dElement.addEventListener('touchstart', (event) => {
        isTouching = true;
        if (event.touches.length === 1) {
            const touch = event.touches[0];
            handleMouseMove({ clientX: touch.clientX, clientY: touch.clientY });
        }
    });
    
    c3dElement.addEventListener('touchmove', (event) => {
        event.preventDefault();
        if (event.touches.length === 1 && isTouching) {
            const touch = event.touches[0];
            handleMouseMove({ clientX: touch.clientX, clientY: touch.clientY });
        }
    });
    
    c3dElement.addEventListener('touchend', () => {
        isTouching = false;
        resetTilt();
    });
}